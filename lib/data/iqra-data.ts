// Helper function to get a specific volume
export function getIqraVolume(volumeNumber: number): IqraVolume | undefined {
  return iqraVolumes.find(v => v.volumeNumber === volumeNumber);
}

// Helper function to get a specific page from a volume
export function getIqraPage(volumeNumber: number, pageNumber: number): IqraPage | undefined {
  const volume = getIqraVolume(volumeNumber);
  if (!volume) return undefined;
  return volume.pages.find(p => p.pageNumber === pageNumber);
}

// Helper function to get total pages in a volume
export function getTotalPages(volumeNumber: number): number {
  const volume = getIqraVolume(volumeNumber);
  return volume ? volume.pages.length : 0;
}
