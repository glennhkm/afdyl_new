// lib/data/iqra-data.ts
// Iqra' learning data - progressive Arabic reading method by K.H. As'ad Humam
// Consists of 6 volumes (jilid) with increasing complexity

export interface IqraLetter {
  arabic: string;          // Arabic letter with diacritics
  transliteration: string; // Romanized pronunciation
  audioText?: string;      // Text to use for TTS (if different from arabic)
}

export interface IqraPage {
  pageNumber: number;
  letters: IqraLetter[];   // 30 letters per page (6 columns × 5 rows)
  highlightIndices?: number[]; // Indices of letters to highlight (first row = header)
}

export interface IqraVolume {
  volumeNumber: number;
  title: string;
  arabicTitle: string;
  description: string;
  pages: IqraPage[];
}

// Helper function to generate letters for a page
// Exported for potential future use in dynamic content generation
export const generateLetters = (
  baseLetters: { arabic: string; latin: string }[],
  harakat: 'fathah' | 'kasrah' | 'dammah'
): IqraLetter[] => {
  const harakatMap = {
    fathah: { mark: 'َ', suffix: 'a' },
    kasrah: { mark: 'ِ', suffix: 'i' },
    dammah: { mark: 'ُ', suffix: 'u' },
  };

  const h = harakatMap[harakat];
  return baseLetters.map(letter => ({
    arabic: letter.arabic + h.mark,
    transliteration: letter.latin === 'alif' ? h.suffix.toUpperCase() : letter.latin.charAt(0).toUpperCase() + h.suffix,
  }));
};

